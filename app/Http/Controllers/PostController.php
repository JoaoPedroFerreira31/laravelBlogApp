     */
    public function show(Request $request, Post $post)
    {
        $post->load('author', 'comments', 'comments.user')->loadCount('comments');
        $author = User::where('id', $post->author)->with('pendingRequests')->first();

        return view('post-show', [
            'post' => $post,
            'author' => $author
        ]);
    }

